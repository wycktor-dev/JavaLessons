public class Atividade1 {
    public static final float PI = 3.14f;
    public static void main(String[] args) {
        boolean resposta;
        resposta = false;
        System.out.println("O valor da resposta é: " + resposta);

        int idade;
        idade = 16;
        System.out.println("A idade é: " + idade);

        float peso;
        peso = 65.5f;
        System.out.println("O peso é: " + peso);

        double altura;
        altura = 1.75;
        System.out.println("A altura é: " + altura);

        char texto;
        texto = 'A';
        System.out.println("O caracter é: " + texto);

        String nome;
        nome = "Ana";
        System.out.println("O nome é: " + nome);

        System.out.println("O valor de PI é: " + PI);

        int num1 = 10;
        int num2 = 5;
        int soma = num1 + num2;
        System.out.println("A soma de " + num1 + " + " + num2 + " é: " + soma);
    }
}
